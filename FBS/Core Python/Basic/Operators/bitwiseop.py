#1. & 
print(10 & 2)
print("------------------")

#2. |
print(10 | 6)
print("------------------")

# 3.XOR( If BOTH operands are DIFFERENT, then only returns ONE)
print(10 ^ 6)
print("------------------")

# 4. NOT
print(~10) # ~x = -(x + 1)
print("------------------")

# 5. << (left shift after converting in bits)
print(10 << 2)
print(6 << 3)
print("------------------")

# 6. >>(right shift after converting in bits)
print(10 >> 2)
print(6 >> 1)
print("------------------")