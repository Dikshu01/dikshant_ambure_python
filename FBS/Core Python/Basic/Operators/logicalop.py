# 1. and
print(True and True)
print(True and False)
print(False and True)
print(False and True)
print('------------------------------')
# (If one of the two conditions is TRUE then only returns TRUE otherwise returns FALSE)

# 2. or
print(True or True)
print(True or False)
print(False or True)
print(False or False)
print('------------------------------')
# (If any of the two operands is TRUE then returns TRUE otherwise returns FALSE)

# 3. not
print(not True)
print(not False)
print(not {}) #(Falsy Values- 0, None, False, blank(all ds))
print(not 10)
print(not 0)
print('------------------------------')