# Write a program to input any alphabet and check whether it is vowel or consonant.
char=input("Enter an alphabet.:")
if(char=="a"):
    print(f"{char} is a vowel.")
elif(char=="e"):
    print(f"{char} is a vowel.")
elif(char=="i"):
    print(f"{char} is a vowel.")
elif(char=="o"):
    print(f"{char} is a vowel.")
elif(char=="u"):
    print(f"{char} is a vowel.")
else:
    print(f"{char} is a consonant.")